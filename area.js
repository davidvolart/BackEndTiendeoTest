class Area {
    constructor(x,y) {
        this.xFinal = x;
        this.yFinal = y;
    }

    getXInicial(){
        return this.xInicial;
    }
  
    getYInicial(){
        return this.yInicial;
    }

    getX(){
        return this.xFinal;
    }

    getY(){
        return this.yFinal;
    }

  }
  
  module.exports = Area